package day3;

public class Expression3 {
	public static void main(String[] args) {
		byte a = 1;
		byte b = 2;
		byte c = 3;
		byte d = (byte)(a + b);
		System.out.printf("a = %d\n", a);
		System.out.printf("b = %d\n", b);
		System.out.printf("c = %d\n", c);
		System.out.printf("d = %d\n", d);
	}
}